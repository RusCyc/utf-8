int main(int argc, char *argv[]) {
    if (argc < 3) {
        printf("Использование: %s <файл> <koi8-r|cp1251|iso8859-5>\n", argv[0]);
        return 1;
    }

    // Выбираем таблицу
    const unsigned int *table;
    if (strcmp(argv[2], "koi8-r") == 0) table = koi8r;
    else if (strcmp(argv[2], "cp1251") == 0) table = cp1251;
    else if (strcmp(argv[2], "iso8859-5") == 0) table = iso88595;
    else {
        printf("Плохая кодировка\n");
        return 1;
    }

    // Читаем файл
    FILE *f = fopen(argv[1], "rb");
    if (!f) {
        printf("Не могу открыть файл\n");
        return 1;
    }

    fseek(f, 0, SEEK_END);
    long size = ftell(f);
    fseek(f, 0, SEEK_SET);

    unsigned char *data = malloc(size);
    fread(data, 1, size, f);
    fclose(f);

    // Создаем выходной файл
    char out[256];
    snprintf(out, sizeof(out), "%s.utf8", argv[1]);
    FILE *out_f = fopen(out, "wb");

    // Преобразуем
    for (long i = 0; i < size; i++) {
        unsigned char c = data[i];
        if (c < 0x80) {
            fputc(c, out_f);
        } else {
            unsigned int u = table[c - 0x80];
            if (u < 0x800) {
                fputc(0xC0 | (u >> 6), out_f);
                fputc(0x80 | (u & 0x3F), out_f);
            } else {
                fputc(0xE0 | (u >> 12), out_f);
                fputc(0x80 | ((u >> 6) & 0x3F), out_f);
                fputc(0x80 | (u & 0x3F), out_f);
            }
        }
    }

    fclose(out_f);
    free(data);
    printf("Сохранено в %s\n", out);
    return 0;
}
